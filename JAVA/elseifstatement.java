public class elseifstatement {

	public static void main(String[] args) {
		String israining="true";
		String iscloudy="false";
		if(israining=="false") {
			System.out.println("weather is raining");
		}
		else if(iscloudy=="true") {
			System.out.println("weather is cloudy");
		}
		else {
			System.out.println("Sky is normal");
		}

	}

}
